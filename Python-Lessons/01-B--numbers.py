# operators you can use in python

import math         # math is an object.
print(10 + 3)
print(10 / 3)       # division. returns floting value.
print(10 // 3)      # division. returns integer vlaue.
print(10 % 3)       # modulus.
print(10 ** 3)      # exponential. so it 10 to the power 3.

# augmenting assignment operator.
x = 10
x = x+3             # usual way.
x += 3              # augmeting assignment operator. shorter way.
print(x)

# some built-in funcitons for math

print(round(2.8))   # rounded value.
print(abs(-2.5))    # absolute value.

# for any advance math import the math module.


# using math as an object to call the available functions. ceil returns ceiling value.
print(math.ceil(2.1))
print(math.factorial(3))
print(math.sqrt(100))
