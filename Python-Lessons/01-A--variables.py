# variables in python

# integer; always add spaces around = operator.
x = 1

# string; always use underscore instead when space is needed.
course_name = "Python"

# boolean; only accepts "True" or "False"; remember python is case-sensitive.
fee_paid = True

# float; always use lowercase letters in python for variables.
course_duration_yrs = 1.5

# """message"""; good for message body.

course_description = """
Hello learner,

This course will teach you basics of python programming language!

Happy Learning!!
"""

print("Course Name : " + course_name + "\n" + course_description)

# above variables are examples of Primitive type. There is also Complex type (e.g. range() used in For lop).
